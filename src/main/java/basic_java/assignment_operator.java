public class assignment_operator {
    public static void main(String[] args) {
        int x =6,y = 3;
        
        x+=y; // x= x+y =9
        System.out.println("Vlaue of X is :"+x);
        
        x-=y; // x= x-y =6
        System.out.println("Vlaue of X is :"+x);
        
        x*=y; // x= x*y =18
        System.out.println("Vlaue of X is :"+x);
        
        x/=y; // x= x/y =6
        System.out.println("Vlaue of X is :"+x);
        
        x%=y; // x= x%y =0
        System.out.println("Vlaue of X is :"+x);
        
//        x store the last assigned value everytime...note that.
    }
    
}
